package com.health.pojo;

public class Diet {
    private Integer id;

    private Double total;

    private Double breakfast;

    private Double lunch;

    private Double dinner;

    private Double sport;

    private Double abreakfast;

    private Double adinner;

    private Double alunch;

    private Double asport;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Double getTotal() {
        return total;
    }

    public void setTotal(Double total) {
        this.total = total;
    }

    public Double getBreakfast() {
        return breakfast;
    }

    public void setBreakfast(Double breakfast) {
        this.breakfast = breakfast;
    }

    public Double getLunch() {
        return lunch;
    }

    public void setLunch(Double lunch) {
        this.lunch = lunch;
    }

    public Double getDinner() {
        return dinner;
    }

    public void setDinner(Double dinner) {
        this.dinner = dinner;
    }

    public Double getSport() {
        return sport;
    }

    public void setSport(Double sport) {
        this.sport = sport;
    }

    public Double getAbreakfast() {
        return abreakfast;
    }

    public void setAbreakfast(Double abreakfast) {
        this.abreakfast = abreakfast;
    }

    public Double getAdinner() {
        return adinner;
    }

    public void setAdinner(Double adinner) {
        this.adinner = adinner;
    }

    public Double getAlunch() {
        return alunch;
    }

    public void setAlunch(Double alunch) {
        this.alunch = alunch;
    }

    public Double getAsport() {
        return asport;
    }

    public void setAsport(Double asport) {
        this.asport = asport;
    }
}